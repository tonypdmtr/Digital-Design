;*******************************************************************************
; Reset memory locations
;*******************************************************************************

START_ADDRESS       equ       $0000
END_ADDRESS         equ       $0100

Vreset              equ       $FFFE

STACKTOP            equ       $01FF
ROM                 equ       $D000

;*******************************************************************************
                    #ROM
;*******************************************************************************
                    org       ROM

Start               proc
                    lds       #STACKTOP
                    bsr       Init_Memory
                    bra       *

;*******************************************************************************

Init_Memory         proc
                    ldx       #START_ADDRESS
Loop@@              clr       ,x
                    inx
                    cpx       #END_ADDRESS
                    bne       Loop@@
                    rts

;*******************************************************************************
                    #VECTORS
;*******************************************************************************

                    org       Vreset
                    dw        Start
